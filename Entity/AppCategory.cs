namespace AppNarcServer.Entity
{
    /// <summary>
    /// An enum that represents the categories of applications.
    /// </summary>
    public enum AppCategory
    {
        /// <summary>
        /// Category for Audio / Video.
        /// </summary>
        AudioVideo,

        /// <summary>
        /// Category for design type applications.
        /// </summary>
        Design,

        /// <summary>
        /// Category for games.
        /// </summary>
        Games,

        /// <summary>
        /// Category for Internet applications (such as web browsers).
        /// </summary>
        Internet,

        /// <summary>
        /// Office applications - such as Microsoft Word, Excel, etc.
        /// </summary>
        Office,

        /// <summary>
        /// Security applications - virus scanners
        /// </summary>
        Security,

        /// <summary>
        /// Utility applications - calc/notepad/etc.
        /// </summary>
        Utility,

        /// <summary>
        /// Productivity applications.
        /// </summary>
        Productivity,

        /// <summary>
        /// Development applications - IDEs, etc.
        /// </summary>
        Development,

        /// <summary>
        /// Social media applications - Facebook, Twitter, etc.
        /// </summary>
        SocialMedia,

        /// <summary>
        /// News / media type applications - Wired.com, WSJ.com, etc.
        /// </summary>
        NewsMedia,
    }
}
